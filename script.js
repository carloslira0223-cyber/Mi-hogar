function getCalendarParts(start, end) {
  let years = end.getFullYear() - start.getFullYear();
  let months = end.getMonth() - start.getMonth();
  let days = end.getDate() - start.getDate();

  if (days < 0) {
    months -= 1;
    const previousMonth = new Date(end.getFullYear(), end.getMonth(), 0);
    days += previousMonth.getDate();
  }

  if (months < 0) {
    years -= 1;
    months += 12;
  }

  return { years, months, days };
}

function updateLoveCounter() {
  if (!loveCounter) return;

  const start = new Date(2025, 1, 20, 0, 0, 0);
  const now = new Date();
  const { years, months, days } = getCalendarParts(start, now);
  const diff = Math.max(0, now - start);
  const totalSeconds = Math.floor(diff / 1000);
  const hours = Math.floor(totalSeconds / 3600) % 24;
  const minutes = Math.floor(totalSeconds / 60) % 60;

  loveCounter.textContent = `${years}a ${months}m ${days}d · ${hours}h ${minutes}m`;
}

