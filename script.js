function openModal(sectionId, trigger = null) {
  if (!modalBackdrop || !sections[sectionId]) return;
  lastFocusedControl = trigger;

  if (isSectionUnlocked(sectionId)) {
    updatePanel(sectionId);
  } else {
    updateLockedPanel(sectionId);
  }

  modalBackdrop.hidden = false;
  document.body.classList.add("modal-open");
  modalClose.focus({ preventScroll: true });
}

function closeModal() {
  if (!modalBackdrop) return;
  modalBackdrop.hidden = true;
  document.body.classList.remove("modal-open");

  if (lastFocusedControl) {
    lastFocusedControl.focus({ preventScroll: true });
    lastFocusedControl = null;
  }
}

