function buildHomeGame(gameData) {
  const found = new Set();
  const needed = gameData.choices.filter((choice) => choice.correct).length;

  const game = document.createElement("section");
  game.className = "home-game";

  const heading = document.createElement("h3");
  heading.textContent = gameData.title;

  const prompt = document.createElement("p");
  prompt.className = "home-game-prompt";
  prompt.textContent = gameData.prompt;

  const progress = document.createElement("div");
  progress.className = "home-game-progress";
  progress.setAttribute("aria-label", "Progreso del juego");

  for (let index = 0; index < needed; index += 1) {
    const dot = document.createElement("span");
    progress.appendChild(dot);
  }

  const choices = document.createElement("div");
  choices.className = "home-game-choices";

  const status = document.createElement("p");
  status.className = "home-game-status";
  status.textContent = "Encuentra 4 pistas.";

  const final = document.createElement("p");
  final.className = "home-game-final";
  final.textContent = gameData.final;
  final.hidden = true;

  function updateProgress() {
    [...progress.children].forEach((dot, index) => {
      dot.classList.toggle("is-found", index < found.size);
    });

    if (found.size === needed) {
      status.textContent = "Listo, ya encontramos el hogar.";
      final.hidden = false;
    } else {
      status.textContent = `Pistas encontradas: ${found.size}/${needed}`;
    }
  }

  gameData.choices.forEach((choice, index) => {
    const button = document.createElement("button");
    button.type = "button";
    button.textContent = choice.label;

    button.addEventListener("click", () => {
      if (choice.correct) {
        found.add(index);
        button.classList.add("is-found");
        button.disabled = true;
        updateProgress();
        return;
      }

      button.classList.add("is-wrong");
      status.textContent = choice.hint;
      setTimeout(() => button.classList.remove("is-wrong"), 550);
    });

    choices.appendChild(button);
  });

  game.append(heading, prompt, progress, choices, status, final);
  updateProgress();
  return game;
}

